package com.knowledgespike.core.type.shared

/**
 * Represents the direction of sorting for a collection or query result.
 *
 * The sort direction can be ascending (ASC) or descending (DESC).
 *
 * - ASC: Indicates that the sorting is in ascending order (e.g., A to Z, 0 to 9).
 * - DESC: Indicates that the sorting is in descending order (e.g., Z to A, 9 to 0).
 */
enum class SortDirection {
    ASC,
    DESC,
}